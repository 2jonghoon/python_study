''' 
1. https://pypi.org/ 에서 패키지를 다운 받을 수 있다.
2. 원하는 패키지를 조회 후 클립보드 복사 한다.
ex) beautifulsoup4 4.11.1 / 2022.12.20 기준
3. VS 터미널에 복사하여 붙여 넣는다.
(pip install beautifulsoup4)

4. 위처럼 터미널에 명령어를 입력하면 해당 패키지가 install 된다.

5. pip list → 현재 설치 되어있는 패키지를 본다. 

6. pip show beautifulsoup4  → 해당 패키지의 정보를 보여준다.

7. pip install --upgrade beautifulsoup4  → 해당 패키지를 upgrade 시킨다.

8. pip uninstall beautifulsoup4  → 해당 패키지를 지운다. (지우는 여부 한번 물음)
'''